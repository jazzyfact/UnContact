package com.example.uncontact.Mypage;

public class mypagePaymentData {
    String title;
    int price;

    public mypagePaymentData(String title, int price) {
        this.title = title;
        this.price = price;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
